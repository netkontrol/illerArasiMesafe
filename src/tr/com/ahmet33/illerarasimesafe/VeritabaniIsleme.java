	/*Tüm kayýtlarý getir:*/
	public List<Il> kayitlariGetir() {
		List<Il> kisilerListesi = new ArrayList<Il>();
		/*tüm kayýtlara sorgu oluþtur:*/
		String selectQuery = "SELECT  * FROM " + TABLO_ISMI;

		SQLiteDatabase db = this.getWritableDatabase();
		Cursor cursor = db.rawQuery(selectQuery, null);
		if (cursor.moveToFirst()) {//ilk kayýt
			do {
				Il kisi = new Il();
				kisi.setID1(Integer.parseInt(cursor.getString(0)));
				kisi.setID2(Integer.parseInt(cursor.getString(1)));
				kisi.setMESAFE(Integer.parseInt(cursor.getString(2)));
			
				/*Kiþiler listesine ekle:*/
				kisilerListesi.add(kisi);
			} while (cursor.moveToNext());
		}
		return kisilerListesi;
	}

